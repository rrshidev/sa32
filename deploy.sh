#!/bin/bash

# Скрипт для обновления приложения на сервере
# Сервер: 104.128.132.83
# Папка: /opt/sa32/

echo "Начинаем обновление приложения на сервере..."

# 1. Останавливаем текущие контейнеры
echo "Останавливаем контейнеры..."
docker-compose down

# 2. Скачиваем последние изменения
echo "Скачиваем изменения из репозитория..."
git pull origin main

# 3. Собираем новые образы
echo "Собираем Docker образы..."
docker-compose build --no-cache

# 4. Применяем миграции базы данных
echo "Применяем миграции..."
docker-compose run --rm backend npm run migration:run

# 5. Запускаем приложение
echo "Запускаем приложение..."
docker-compose up -d

# 6. Проверяем статус
echo "Проверяем статус контейнеров..."
docker-compose ps

echo "Обновление завершено!"
